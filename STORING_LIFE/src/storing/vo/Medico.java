package storing.vo;

public class Medico {
	
	private String crmMedico;
	private String nomeMedico;
	private String hospAtuacao;
	private String senhaMedico;
	private String dirMedico;
	
	
	public String getCrmMedico() {
		return crmMedico;
	}
	public void setCrmMedico(String crmMedico) {
		this.crmMedico = crmMedico;
	}
	public String getNomeMedico() {
		return nomeMedico;
	}
	public void setNomeMedico(String nomeMedico) {
		this.nomeMedico = nomeMedico;
	}
	public String getHospAtuacao() {
		return hospAtuacao;
	}
	public void setHospAtuacao(String hospAtuacao) {
		this.hospAtuacao = hospAtuacao;
	}
	public String getSenhaMedico() {
		return senhaMedico;
	}
	public void setSenhaMedico(String senhaMedico) {
		this.senhaMedico = senhaMedico;
	}
	public String getDirMedico() {
		return dirMedico;
	}
	public void setDirMedico(String dirMedico) {
		this.dirMedico = dirMedico;
	}

}
